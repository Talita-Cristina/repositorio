# Em Python, não precisamos declarar o tipo r tamanho fixo
# do vetor antecipadamente.

# Cria uma lista com 4 elementos, inicializados com 0
vetorDeInteiros = [0] * 4 #vetor que vai do índice 0 até 4

vetorDeInteiros[0] = 2

# Usamos f-strings para formatar a saída de forma concisa.
print(f"Na posição 0, o valor é.: {vetorDeInteiros[0]}")